package JAVA;

public class Lab026_Arryprint {
    public static void main(String[] args) {
        String arr []={"mukesh","dhage"};

        for (int i = 0; i <arr.length ; i++) {
            System.out.println(arr[i]);
        }

        for (int i =arr.length-1; i >=0 ; i--) {
            System.out.println(arr[i]);
        }
    }
}
